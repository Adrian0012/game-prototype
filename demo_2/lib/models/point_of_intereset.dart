class PointOfInterest {
  final double x;
  final double y;
  final String label;
  final bool isAnimation;
  final String asset;
  final double assetSize;
  final double assetSizeX;
  final double assetSizeY;
  final int frameCount;
  PointOfInterest({
    required this.x,
    required this.y,
    required this.label,
    required this.isAnimation,
    required this.asset,
    required this.assetSize,
    required this.assetSizeX,
    required this.assetSizeY,
    required this.frameCount,
  });
}
